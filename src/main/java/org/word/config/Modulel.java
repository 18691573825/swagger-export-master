package org.word.config;

public class Modulel {
    private String tag;
    private String swaggerUrl;
    private String exportDocName;

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getSwaggerUrl() {
        return swaggerUrl;
    }

    public void setSwaggerUrl(String swaggerUrl) {
        this.swaggerUrl = swaggerUrl;
    }

    public String getExportDocName() {
        return exportDocName;
    }

    public void setExportDocName(String exportDocName) {
        this.exportDocName = exportDocName;
    }

}
